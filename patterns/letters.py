def get_letter_pattern(letter):
    """Obtiene el patrón para una letra específica"""
    return LETTERS.get(letter.upper(), [0]*8)

def get_word_pattern(word, max_length=2):
    """Obtiene patrones para una palabra (máximo 2 letras para display 8x16)"""
    word = word.upper()[:max_length]
    patterns = []
    for letter in word:
        patterns.append(get_letter_pattern(letter))
    return patterns

def get_available_letters():
    """Retorna la lista de todas las letras disponibles"""
    return list(LETTERS.keys())

def get_letter_info(letter):
    """Retorna información sobre una letra específica"""
    letter = letter.upper()
    if letter in LETTERS:
        return {
            'letter': letter,
            'pattern': LETTERS[letter],
            'exists': True
        }
    else:
        return {
            'letter': letter,
            'pattern': [0]*8,
            'exists': False
        } 