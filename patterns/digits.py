def get_digit_pattern(digit):
    """Obtiene el patrón para un dígito específico"""
    return DIGITS.get(str(digit), [0]*8)

def get_two_digits_pattern(value):
    """Obtiene patrones para un número de dos dígitos"""
    s = str(value)
    if len(s) == 1:
        s = "0" + s
    
    left_pattern = DIGITS.get(s[0], [0]*8)
    right_pattern = DIGITS.get(s[1], [0]*8)
    
    return left_pattern, right_pattern 